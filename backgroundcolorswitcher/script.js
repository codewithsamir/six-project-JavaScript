
document.addEventListener("click",function(e){
let select = e.target.id
document.body.style.background = "#fff";
 
if(select === "red"){
    document.body.style.background = select;
}
if(select === "yellow"){
    document.body.style.background = select;
}
if(select === "blue"){
    document.body.style.background = select;
}
if(select === "green"){
    document.body.style.background = select;
}
if(select === "pink"){
    document.body.style.background = select;
}

})